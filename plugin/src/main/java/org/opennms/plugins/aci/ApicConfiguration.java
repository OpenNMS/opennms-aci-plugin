package org.opennms.plugins.aci;

/**
 * @author metispro
 *
 */
public class ApicConfiguration {
    
    private String location;
    
    private String apicUrl;
    
    private String username;
    
    private String password;

    public ApicConfiguration(String location, String apicUrl, String username,
            String password) {
        super();
        this.location = location;
        this.apicUrl = apicUrl;
        this.username = username;
        this.password = password;
    }

    /**
     * @return the location
     */
    public String getLocation() {
        return location;
    }

    /**
     * @param location the location to set
     */
    public void setLocation(String location) {
        this.location = location;
    }

    /**
     * @return the apicUrl
     */
    public String getApicUrl() {
        return apicUrl;
    }

    /**
     * @param apicUrl the apicUrl to set
     */
    public void setApicUrl(String apicUrl) {
        this.apicUrl = apicUrl;
    }

    /**
     * @return the username
     */
    public String getUsername() {
        return username;
    }

    /**
     * @param username the username to set
     */
    public void setUsername(String username) {
        this.username = username;
    }

    /**
     * @return the password
     */
    public String getPassword() {
        return password;
    }

    /**
     * @param password the password to set
     */
    public void setPassword(String password) {
        this.password = password;
    }

}
